----- Inserindo Valores de Pagamentos------

INSERT INTO Pagamentos (IdPedido, Forma_de_Pagamento, Valor, Status_Pagamento, Data_Pagamento)
SELECT
    p.IdPedido,
    CASE FLOOR(1 + (RAND() * 3))
        WHEN 1 THEN 'Cartão'
        WHEN 2 THEN 'Pix'
        ELSE 'Boleto'
    END AS Forma_de_Pagamento,
    SUM(ip.Quantidade * ip.Preço_Unitario - ip.Desconto) AS Valor,
    CASE FLOOR(1 + (RAND() * 3))
        WHEN 1 THEN 'Pendente'
        WHEN 2 THEN 'Aprovado'
        ELSE 'Recusado'
    END AS Status_Pagamento,
    DATE_ADD(p.Data_Pedido, INTERVAL FLOOR(RAND() * 10) DAY) AS Data_Pagamento
FROM Pedido p
JOIN Item_Pedido ip ON p.IdPedido = ip.IdPedido
GROUP BY p.IdPedido;



