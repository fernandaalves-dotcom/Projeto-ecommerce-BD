INSERT INTO Entrega (idCliente, idPedido, idProduto, Telefone, Tipo_Cliente, Descrição, Endereço_de_Entrega)
SELECT 
    c.idCliente,
    ip.IdPedido,
    ip.IdProduto,
    c.Telefone,
    c.Tipo_Cliente,
    p.Descrição,
    CASE 
        WHEN c.Tipo_Cliente = 'PF' THEN cpf.`Endereço_de_Entrega`
        WHEN c.Tipo_Cliente = 'PJ' THEN cpj.`Endereço_Comercial`
    END
FROM Item_Pedido ip
JOIN Pedido pd ON ip.IdPedido = pd.IdPedido
JOIN Produto p ON ip.IdProduto = p.idProduto
JOIN Cliente c ON pd.IdCliente = c.idCliente
LEFT JOIN Cliente_PF cpf ON c.idCliente = cpf.IdCliente
LEFT JOIN Cliente_PJ cpj ON c.idCliente = cpj.IdCliente;
